    private void btnViewOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrderActionPerformed
        
        int rowSelected = tblOrders.getSelectedRow();
        
        if(rowSelected == -1)
        {
            JOptionPane.showMessageDialog(null, "Select An Order");
        }
        else
        {
            int orderId = Integer.parseInt(String.valueOf(tblOrders.getValueAt(rowSelected, 0)));
            Order selectedOrder = loggedInCustomer.getOrders().get(orderId);
            
            ViewPreviousOrderLines vpol = new ViewPreviousOrderLines(loggedInCustomer, selectedOrder);
            this.dispose();
            vpol.setVisible(true);
        }
    }//GEN-LAST:event_btnViewOrderActionPerformed

